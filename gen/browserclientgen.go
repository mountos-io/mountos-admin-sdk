package main

import (
	"fmt"
	"path/filepath"
	"strings"
)

func generateBrowserClient(spec *Spec, outDir string) {
	if outDir == "" {
		return
	}
	var w strings.Builder
	w.WriteString("// Code generated by gen; DO NOT EDIT.\n\n")

	// Imports
	w.WriteString("import { ApiError } from './errors.js'\n")
	w.WriteString("import type {\n")
	w.WriteString("  StandardResponse, ListOptions,\n")
	w.WriteString("  PaginatedResponse, CursorPaginatedResponse,\n")

	var typeImports []string
	typeSet := make(map[string]bool)
	addImport := func(name string) {
		if !typeSet[name] {
			typeSet[name] = true
			typeImports = append(typeImports, name)
		}
	}

	for _, res := range spec.Resources {
		for _, ep := range res.Endpoints {
			if len(ep.Request) > 0 {
				addImport(requestTypeName(res.Name, ep.Action))
			}
			if ep.ResponseType != "" {
				addImport(ep.ResponseType)
			}
			if ep.Pagination == "page" && hasRequiredQueryParam(ep.Query) {
				addImport(listOptionsTypeName(res.Name))
			}
			if ep.Pagination == "cursor" {
				addImport(listOptionsTypeName(res.Name))
			}
		}
	}

	line := "  "
	for i, t := range typeImports {
		if i > 0 {
			line += ", "
		}
		if len(line)+len(t) > 90 {
			w.WriteString(line + "\n")
			line = "  "
		}
		line += t
	}
	if len(line) > 2 {
		line += ","
		w.WriteString(line + "\n")
	}
	w.WriteString("} from '@mountos-io/admin-sdk'\n\n")

	// queryString helper
	w.WriteString("function queryString(params: Record<string, string | number | undefined>): string {\n")
	w.WriteString("  const entries = Object.entries(params).filter(([, v]) => v !== undefined)\n")
	w.WriteString("  if (entries.length === 0) return ''\n")
	w.WriteString("  return '?' + entries.map(([k, v]) => `${k}=${encodeURIComponent(v!)}`).join('&')\n")
	w.WriteString("}\n\n")

	// ClientConfig
	w.WriteString("export interface ClientConfig {\n")
	w.WriteString("  baseUrl?: string\n")
	w.WriteString("  getHeaders?: () => Record<string, string> | Promise<Record<string, string>>\n")
	w.WriteString("  onUnauthorized?: () => void\n")
	w.WriteString("}\n\n")

	// AdminClient class
	w.WriteString("export class AdminClient {\n")
	w.WriteString("  readonly baseUrl: string\n")
	w.WriteString("  private readonly _getHeaders: () => Record<string, string> | Promise<Record<string, string>>\n")
	w.WriteString("  private readonly _onUnauthorized?: () => void\n\n")

	for _, res := range spec.Resources {
		slug := slugify(res.Name)
		resClass := res.Name + "Resource"
		fmt.Fprintf(&w, "  private _%s?: %s\n", slug, resClass)
	}
	w.WriteString("\n")

	w.WriteString("  constructor(config: ClientConfig = {}) {\n")
	w.WriteString("    this.baseUrl = (config.baseUrl ?? '/api/proxy/v1').replace(/\\/+$/, '')\n")
	w.WriteString("    this._getHeaders = config.getHeaders ?? (() => ({}))\n")
	w.WriteString("    this._onUnauthorized = config.onUnauthorized\n")
	w.WriteString("  }\n")

	for _, res := range spec.Resources {
		slug := slugify(res.Name)
		resClass := res.Name + "Resource"
		w.WriteString("\n")
		fmt.Fprintf(&w, "  get %s(): %s {\n", slug, resClass)
		fmt.Fprintf(&w, "    return (this._%s ??= new %s(this))\n", slug, resClass)
		w.WriteString("  }\n")
	}

	// request method
	w.WriteString("\n")
	w.WriteString("  async request<T>(method: string, path: string, body?: unknown): Promise<T> {\n")
	w.WriteString("    const extra = await this._getHeaders()\n")
	w.WriteString("    const headers: Record<string, string> = { ...extra }\n\n")
	w.WriteString("    const init: RequestInit = { method, headers, credentials: 'include' }\n")
	w.WriteString("    if (body !== undefined) {\n")
	w.WriteString("      headers['Content-Type'] = 'application/json'\n")
	w.WriteString("      init.body = JSON.stringify(body)\n")
	w.WriteString("    }\n\n")
	w.WriteString("    const res = await fetch(`${this.baseUrl}${path}`, init)\n\n")
	w.WriteString("    if (res.status === 401) {\n")
	w.WriteString("      this._onUnauthorized?.()\n")
	w.WriteString("      throw new ApiError('unauthorized', 401)\n")
	w.WriteString("    }\n\n")
	w.WriteString("    let json: StandardResponse<T>\n")
	w.WriteString("    try {\n")
	w.WriteString("      json = await res.json() as StandardResponse<T>\n")
	w.WriteString("    } catch {\n")
	w.WriteString("      throw new ApiError(res.statusText || 'request failed', res.status)\n")
	w.WriteString("    }\n\n")
	w.WriteString("    if (json.status !== 'success') {\n")
	w.WriteString("      throw new ApiError(json.message, res.status, json.errorCode)\n")
	w.WriteString("    }\n")
	w.WriteString("    return json.data as T\n")
	w.WriteString("  }\n")
	w.WriteString("}\n")

	// Resource classes (paths without spec.BasePath)
	for _, res := range spec.Resources {
		w.WriteString("\n")
		writeBrowserResource(&w, spec, res)
	}

	writeFile(filepath.Join(outDir, "client.gen.ts"), w.String())
}

func writeBrowserResource(w *strings.Builder, spec *Spec, res Resource) {
	resClass := res.Name + "Resource"
	fmt.Fprintf(w, "class %s {\n", resClass)
	w.WriteString("  constructor(private client: AdminClient) {}\n")

	// Resource path only (no basePath prefix — baseUrl provides the prefix)
	fullBasePath := res.Path
	resPathParams := extractPathParams(fullBasePath)

	for _, ep := range res.Endpoints {
		w.WriteString("\n")
		writeTSMethod(w, spec, res, ep, fullBasePath, resPathParams)
	}
	w.WriteString("}\n")
}
