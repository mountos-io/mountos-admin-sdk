// GET with page pagination
func writeGoPageListMethod(w *strings.Builder, svcType, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	hasCustomOpts := hasExtraQueryParam(ep.Query)
	pathParams := goMethodParams(allPathParams, pt)
	sig := "ctx context.Context"
	if pathParams != "" {
		sig += ", " + pathParams
	}

	var optsType string
	if hasCustomOpts {
		optsType = listOptionsTypeName(resName)
	} else {
		optsType = "ListOptions"
	}

	sig += ", opts *" + optsType
	pathExpr := goPathExpr(fullPath, allPathParams, pt)

	fmt.Fprintf(w, "func (s *%s) %s(%s) (*PaginatedResponse[%s], error) {\n", svcType, methodName, sig, ep.ResponseType)
	w.WriteString("\tq := url.Values{}\n")
	w.WriteString("\tif opts != nil {\n")

	if hasCustomOpts {
		for _, qs := range ep.Query {
			f := parseField(qs)
			if f.Name == "page" || f.Name == "limit" {
				continue
			}
			goName := goFieldName(f.Name)
			if f.Required {
				switch f.Type {
				case "int64":
					fmt.Fprintf(w, "\t\tq.Set(%q, strconv.FormatInt(opts.%s, 10))\n", f.Name, goName)
				case "int":
					fmt.Fprintf(w, "\t\tq.Set(%q, strconv.Itoa(opts.%s))\n", f.Name, goName)
				case "int32":
					fmt.Fprintf(w, "\t\tq.Set(%q, strconv.FormatInt(int64(opts.%s), 10))\n", f.Name, goName)
				case "bool":
					fmt.Fprintf(w, "\t\tq.Set(%q, strconv.FormatBool(opts.%s))\n", f.Name, goName)
				default:
					fmt.Fprintf(w, "\t\tq.Set(%q, opts.%s)\n", f.Name, goName)
				}
			} else {
				writeGoOptionalQuerySet(w, f, goName)
			}
		}
	}

	w.WriteString("\t\taddPagination(q, opts.Page, opts.Limit)\n")
	w.WriteString("\t}\n")

	if hasRequiredQueryParam(ep.Query) {
		fmt.Fprintf(w, "\tdata, err := s.c.get(ctx, %s+\"?\"+q.Encode())\n", pathExpr)
	} else {
		w.WriteString(fmt.Sprintf("\tpath := %s\n", pathExpr))
		w.WriteString("\tif qs := q.Encode(); qs != \"\" {\n")
		w.WriteString("\t\tpath += \"?\" + qs\n")
		w.WriteString("\t}\n")
		w.WriteString("\tdata, err := s.c.get(ctx, path)\n")
	}

	w.WriteString("\tif err != nil {\n\t\treturn nil, err\n\t}\n")
	fmt.Fprintf(w, "\treturn decodeJSON[PaginatedResponse[%s]](data)\n", ep.ResponseType)
	w.WriteString("}\n")
}

