// goImports helper
type goImports struct {
	pkgs map[string]bool
}

func newGoImports() *goImports {
	return &goImports{pkgs: make(map[string]bool)}
}

func (g *goImports) add(pkg string) {
	g.pkgs[pkg] = true
}

func (g *goImports) render() string {
	if len(g.pkgs) == 0 {
		return ""
	}
	var sorted []string
	for p := range g.pkgs {
		sorted = append(sorted, p)
	}
	sort.Strings(sorted)

	if len(sorted) == 1 {
		return fmt.Sprintf("import %q\n\n", sorted[0])
	}

	var b strings.Builder
	b.WriteString("import (\n")
	for _, p := range sorted {
		fmt.Fprintf(&b, "\t%q\n", p)
	}
	b.WriteString(")\n\n")
	return b.String()
}
