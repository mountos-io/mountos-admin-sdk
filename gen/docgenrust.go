package main

import (
	"fmt"
	"path/filepath"
	"strings"
)

func generateDocRust(spec *Spec, outDir string) {
	var w strings.Builder

	w.WriteString("<!-- @generated by gen/docgenrust.go from api.yaml. DO NOT EDIT. -->\n\n")
	w.WriteString("# mountOS Admin SDK - Rust API Reference\n\n")
	w.WriteString("Generated from [api.yaml](../api.yaml). Do not edit.\n\n")
	w.WriteString("- Crate: `mountos-admin-sdk`\n")
	fmt.Fprintf(&w, "- Spec version: `%s`\n", spec.Version)
	fmt.Fprintf(&w, "- Base path: `%s`\n\n", spec.BasePath)

	w.WriteString("## Quick Start\n\n")
	w.WriteString("```rust\n")
	w.WriteString("use mountos_admin_sdk::{Client, Config};\n\n")
	w.WriteString("let client = Client::new(Config {\n")
	w.WriteString("    base_url: \"https://appserv.example.com\".into(),\n")
	w.WriteString("    private_key: \"<base64 ED25519 key>\".into(),\n")
	w.WriteString("    ..Default::default()\n")
	w.WriteString("})?;\n")
	w.WriteString("let account = client.accounts.get(account_id).await?;\n")
	w.WriteString("```\n\n")

	w.WriteString("## Response Model\n\n")
	w.WriteString("Each method returns the unwrapped `data` payload as a typed value, or an [`Error`] ")
	w.WriteString("on `status=\"failure\"` or transport failure. List endpoints return `PaginatedResponse<T>` ")
	w.WriteString("or `CursorPaginatedResponse<T>`; create/edit/toggle endpoints return `IdResponse`.\n\n")
	w.WriteString("```rust\n")
	w.WriteString("pub enum Error {\n")
	w.WriteString("    Api { message: String, status: u16, error_code: i64 },\n")
	w.WriteString("    Http(reqwest::Error),\n")
	w.WriteString("    Serde(serde_json::Error),\n")
	w.WriteString("    Key(String),\n")
	w.WriteString("}\n\n")
	w.WriteString("pub struct PaginatedResponse<T> {\n")
	w.WriteString("    pub items: Vec<T>,\n")
	w.WriteString("    pub pagination: PaginationMeta,\n")
	w.WriteString("}\n\n")
	w.WriteString("pub struct PaginationMeta {\n")
	w.WriteString("    pub page: i64,\n")
	w.WriteString("    pub limit: i64,\n")
	w.WriteString("    pub total: i64,\n")
	w.WriteString("    pub total_pages: i64,\n")
	w.WriteString("}\n\n")
	w.WriteString("pub struct CursorPaginatedResponse<T> {\n")
	w.WriteString("    pub items: Vec<T>,\n")
	w.WriteString("    pub next_cursor: Option<i64>,\n")
	w.WriteString("}\n\n")
	w.WriteString("pub struct IdResponse {\n")
	w.WriteString("    pub id: i64,\n")
	w.WriteString("}\n")
	w.WriteString("```\n\n")

	w.WriteString("## Error Codes\n\n")
	w.WriteString("| Code  | Name                          |\n")
	w.WriteString("|-------|-------------------------------|\n")
	for _, ec := range spec.ErrorCodes {
		fmt.Fprintf(&w, "| %d | %s |\n", ec.Code, ec.Name)
	}
	w.WriteString("\n")

	if len(spec.Enums) > 0 {
		w.WriteString("## Enums\n\n")
		w.WriteString("String aliases; the listed constants are the accepted values.\n\n")
		for _, name := range sortedKeys(spec.Enums) {
			fmt.Fprintf(&w, "### `%s`\n\n", name)
			w.WriteString("```rust\n")
			fmt.Fprintf(&w, "pub type %s = String;\n", name)
			for _, v := range spec.Enums[name] {
				constName := screamingSnake(name) + "_" + screamingSnake(v)
				fmt.Fprintf(&w, "pub const %s: &str = %q;\n", constName, v)
			}
			w.WriteString("```\n\n")
		}
	}

	if len(spec.Types) > 0 {
		w.WriteString("## Types\n\n")
		for _, name := range sortedKeys(spec.Types) {
			fmt.Fprintf(&w, "### `%s`\n\n", name)
			w.WriteString("```rust\n")
			writeRustDocStruct(&w, name, spec.Types[name], false)
			w.WriteString("```\n\n")
		}
	}

	w.WriteString("## Resources\n\n")
	for _, res := range spec.Resources {
		fmt.Fprintf(&w, "### %s\n\n", res.Name)
		fmt.Fprintf(&w, "Accessor: `client.%s`\n\n", snakeCase(res.Name))

		fullBase := spec.BasePath + res.Path
		for _, ep := range res.Endpoints {
			fullPath := strings.TrimSuffix(fullBase+ep.Path, "/")
			methodName := rustFieldName(ep.Action)
			fmt.Fprintf(&w, "#### `%s` - %s %s\n\n", methodName, ep.Method, fullPath)
			w.WriteString("```rust\n")
			fmt.Fprintf(&w, "pub async fn %s(&self%s) -> Result<%s, Error>\n",
				methodName, rustDocArgs(res, ep, fullPath), rustDocReturn(ep, res.Name))
			w.WriteString("```\n\n")

			if len(ep.Request) > 0 {
				w.WriteString("Request body:\n\n```rust\n")
				writeRustDocStruct(&w, requestTypeName(res.Name, ep.Action), ep.Request, true)
				w.WriteString("```\n\n")
			}
			// Query params: only when the real signature takes a
			// resource-specific *ListOptions (not the shared ListOptions),
			// mirroring rustDocArgs's own hasExtraQueryParam gate -- the
			// method signature already names this type, so its shape
			// belongs here rather than nowhere.
			switch {
			case ep.Pagination == "page" && hasExtraQueryParam(ep.Query):
				w.WriteString("Query params:\n\n```rust\n")
				writeRustDocStruct(&w, listOptionsTypeName(res.Name), ep.Query, true)
				w.WriteString("```\n\n")
			case ep.Pagination == "cursor":
				w.WriteString("Query params:\n\n```rust\n")
				writeRustDocStruct(&w, listOptionsTypeName(res.Name), ep.Query, true)
				w.WriteString("```\n\n")
			}
			// Response body: only for a named custom response struct (not
			// IdResponse, and not a primitive/array/paginated return, all of
			// which are already fully visible in the signature itself).
			if !ep.ResponseArray && ep.Pagination == "" && ep.ResponseType == "" &&
				len(ep.Response) > 0 && !responseIsIDOnly(ep.Response) {
				w.WriteString("Response body:\n\n```rust\n")
				writeRustDocStruct(&w, customResponseTypeName(res.Name, ep.Action), ep.Response, false)
				w.WriteString("```\n\n")
			}
		}
	}

	writeFile(filepath.Join(outDir, "rust.md"), w.String())
}

func writeRustDocStruct(w *strings.Builder, name string, fields []string, isRequest bool) {
	fmt.Fprintf(w, "pub struct %s {\n", name)
	for _, fs := range fields {
		f := parseField(fs)
		optional := f.Optional
		if isRequest {
			optional = !f.Required
		}
		ty := rustType(f.Type)
		if optional {
			ty = "Option<" + ty + ">"
		}
		fmt.Fprintf(w, "    pub %s: %s,\n", rustFieldName(f.Name), ty)
	}
	w.WriteString("}\n")
}

// rustDocArgs renders the argument list (after &self) for a doc signature.
func rustDocArgs(res Resource, ep Endpoint, fullPath string) string {
	var args []string
	for _, p := range extractPathParams(fullPath) {
		raw := strings.TrimPrefix(p, ":")
		args = append(args, rustParamName(p)+": "+rustParamType(raw, res.PathParamTypes))
	}

	switch {
	case ep.ResponseArray, (len(ep.Query) > 0 && ep.Pagination == ""):
		for _, qs := range ep.Query {
			f := parseField(qs)
			args = append(args, rustFieldName(f.Name)+": "+rustQueryParamType(f))
		}
	case ep.Pagination == "page":
		optsType := "ListOptions"
		if hasExtraQueryParam(ep.Query) {
			optsType = listOptionsTypeName(res.Name)
		}
		optsParam, _ := rustOptsParam(optsType, ep.Query)
		args = append(args, optsParam)
	case ep.Pagination == "cursor":
		optsParam, _ := rustOptsParam(listOptionsTypeName(res.Name), ep.Query)
		args = append(args, optsParam)
	case len(ep.Request) > 0:
		args = append(args, "req: &"+requestTypeName(res.Name, ep.Action))
	}

	if len(args) == 0 {
		return ""
	}
	return ", " + strings.Join(args, ", ")
}

func rustDocReturn(ep Endpoint, resName string) string {
	switch {
	case ep.ResponseArray:
		return "Vec<" + rustType(ep.ResponseType) + ">"
	case ep.Pagination == "page":
		return "PaginatedResponse<" + rustType(ep.ResponseType) + ">"
	case ep.Pagination == "cursor":
		return "CursorPaginatedResponse<" + rustType(ep.ResponseType) + ">"
	case ep.ResponseType != "":
		return rustType(ep.ResponseType)
	case len(ep.Response) > 0:
		return rustReturnResponseType(ep, resName)
	default:
		return "()"
	}
}
