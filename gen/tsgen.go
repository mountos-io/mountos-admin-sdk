package main

import (
	"fmt"
	"path/filepath"
	"strings"
)

func generateTS(spec *Spec, outDir string) {
	generateTSTypes(spec, outDir)
	generateTSClient(spec, outDir)
}

func isPrimitiveType(specType string) bool {
	switch specType {
	case "string", "datetime", "int64", "int32", "int", "float", "float64", "bool", "object", "json":
		return true
	}
	return false
}

// baseTypeName strips array suffix and optional marker from a spec type string,
// returning the bare type name suitable for import resolution.
func baseTypeName(specType string) string {
	t := strings.TrimSuffix(specType, "?")
	for strings.HasSuffix(t, "[]") {
		t = t[:len(t)-2]
	}
	return t
}

func tsType(specType string) string {
	if strings.HasSuffix(specType, "[]") {
		inner := specType[:len(specType)-2]
		return tsType(inner) + "[]"
	}
	switch specType {
	case "string", "datetime":
		return "string"
	case "int64", "int32", "int", "float", "float64":
		return "number"
	case "bool":
		return "boolean"
	case "object":
		return "Record<string, unknown>"
	case "json":
		return "unknown"
	default:
		return specType
	}
}

func generateTSTypes(spec *Spec, outDir string) {
	var w strings.Builder
	w.WriteString("// Code generated by gen; DO NOT EDIT.\n\n")

	// Config
	w.WriteString("export interface Config {\n")
	w.WriteString("  baseUrl: string\n")
	w.WriteString("  privateKey: string\n")
	w.WriteString("  dashboardUser?: DashboardUser\n")
	w.WriteString("}\n\n")

	// StandardResponse
	w.WriteString("export interface StandardResponse<T = unknown> {\n")
	w.WriteString("  status: 'success' | 'failure'\n")
	w.WriteString("  message: string\n")
	w.WriteString("  data?: T\n")
	w.WriteString("  errorCode?: number\n")
	w.WriteString("}\n\n")

	// PaginationMeta
	w.WriteString("export interface PaginationMeta {\n")
	w.WriteString("  page: number\n")
	w.WriteString("  limit: number\n")
	w.WriteString("  total: number\n")
	w.WriteString("  totalPages: number\n")
	w.WriteString("}\n\n")

	// PaginatedResponse
	w.WriteString("export interface PaginatedResponse<T> {\n")
	w.WriteString("  items: T[]\n")
	w.WriteString("  pagination: PaginationMeta\n")
	w.WriteString("}\n\n")

	// CursorPaginatedResponse
	w.WriteString("export interface CursorPaginatedResponse<T> {\n")
	w.WriteString("  items: T[]\n")
	w.WriteString("  nextCursor: number | null\n")
	w.WriteString("}\n\n")

	// ListOptions
	w.WriteString("export interface ListOptions {\n")
	w.WriteString("  page?: number\n")
	w.WriteString("  limit?: number\n")
	w.WriteString("}\n")

	// Enum types
	enumOrder := orderEnums(spec)
	for _, name := range enumOrder {
		values := spec.Enums[name]
		w.WriteString("\nexport type " + name + " = ")
		for i, v := range values {
			if i > 0 {
				w.WriteString(" | ")
			}
			w.WriteString("'" + v + "'")
		}
		w.WriteString("\n")
	}

	// Model types
	typeOrder := orderTypes(spec)
	for _, typeName := range typeOrder {
		fields, ok := spec.Types[typeName]
		if !ok {
			continue
		}
		w.WriteString("\n")
		writeTSInterface(&w, typeName, fields, false)
	}

	// Request types, response types, list options
	for _, res := range spec.Resources {
		w.WriteString("\n// " + res.Name + "\n")
		for _, ep := range res.Endpoints {
			if len(ep.Request) > 0 {
				reqName := requestTypeName(res.Name, ep.Action)
				w.WriteString("\n")
				writeTSInterface(&w, reqName, ep.Request, true)
			}
		}
		// Custom list options for page pagination with extra query params
		for _, ep := range res.Endpoints {
			if ep.Pagination == "page" && hasExtraQueryParam(ep.Query) {
				optName := listOptionsTypeName(res.Name)
				w.WriteString("\n")
				writeTSListOptions(&w, optName, ep.Query, true)
			}
			if ep.Pagination == "cursor" {
				optName := listOptionsTypeName(res.Name)
				w.WriteString("\n")
				writeTSListOptions(&w, optName, ep.Query, false)
			}
		}
	}

	writeFile(filepath.Join(outDir, "types_gen.ts"), w.String())
}

func writeTSInterface(w *strings.Builder, name string, fields []string, isRequest bool) {
	w.WriteString("export interface " + name + " {\n")
	for _, s := range fields {
		f := parseField(s)
		optional := ""
		if isRequest {
			if !f.Required {
				optional = "?"
			}
		} else if f.Optional {
			optional = "?"
		}
		fmt.Fprintf(w, "  %s%s: %s\n", f.Name, optional, tsType(f.Type))
	}
	w.WriteString("}\n")
}

func writeTSListOptions(w *strings.Builder, name string, query []string, extendsListOptions bool) {
	if extendsListOptions {
		w.WriteString("export interface " + name + " extends ListOptions {\n")
		for _, s := range query {
			f := parseField(s)
			if f.Name == "page" || f.Name == "limit" {
				continue
			}
			optional := ""
			if !f.Required {
				optional = "?"
			}
			fmt.Fprintf(w, "  %s%s: %s\n", f.Name, optional, tsType(f.Type))
		}
	} else {
		w.WriteString("export interface " + name + " {\n")
		for _, s := range query {
			f := parseField(s)
			fmt.Fprintf(w, "  %s?: %s\n", f.Name, tsType(f.Type))
		}
	}
	w.WriteString("}\n")
}

func generateTSClient(spec *Spec, outDir string) {
	var w strings.Builder
	w.WriteString("// Code generated by gen; DO NOT EDIT.\n\n")

	// Imports
	w.WriteString("import type {\n")
	w.WriteString("  ListOptions, PaginatedResponse, CursorPaginatedResponse,\n")

	// Collect all type imports
	var typeImports []string
	typeSet := make(map[string]bool)
	addImport := func(name string) {
		if !typeSet[name] && !isPrimitiveType(name) {
			typeSet[name] = true
			typeImports = append(typeImports, name)
		}
	}

	for _, res := range spec.Resources {
		for _, ep := range res.Endpoints {
			if len(ep.Request) > 0 {
				addImport(requestTypeName(res.Name, ep.Action))
			}
			if ep.ResponseType != "" {
				addImport(ep.ResponseType)
			}
			for _, s := range ep.Response {
				f := parseField(s)
				addImport(baseTypeName(f.Type))
			}
			for _, s := range ep.Request {
				f := parseField(s)
				addImport(baseTypeName(f.Type))
			}
			if ep.Pagination == "page" && hasExtraQueryParam(ep.Query) {
				addImport(listOptionsTypeName(res.Name))
			}
			if ep.Pagination == "cursor" {
				addImport(listOptionsTypeName(res.Name))
			}
		}
	}

	// Write type imports in chunks
	line := "  "
	for i, t := range typeImports {
		if i > 0 {
			line += ", "
		}
		if len(line)+len(t) > 90 {
			w.WriteString(line + "\n")
			line = "  "
		}
		line += t
	}
	if len(line) > 2 {
		line += ","
		w.WriteString(line + "\n")
	}
	w.WriteString("} from './types_gen.js'\n\n")

	// RequestFn + internal Client
	w.WriteString("export type RequestFn = <T>(method: string, path: string, body?: unknown, signal?: AbortSignal) => Promise<T>\n\n")
	w.WriteString("interface Client { request: RequestFn }\n\n")

	// queryString helper
	w.WriteString("function queryString(params: Record<string, string | number | boolean | undefined>): string {\n")
	w.WriteString("  const entries = Object.entries(params).filter(([, v]) => v !== undefined)\n")
	w.WriteString("  if (entries.length === 0) return ''\n")
	w.WriteString("  return '?' + entries.map(([k, v]) => `${k}=${encodeURIComponent(v!)}`).join('&')\n")
	w.WriteString("}\n\n")

	// AdminClient interface
	w.WriteString("export interface AdminClient {\n")
	for _, res := range spec.Resources {
		slug := slugify(res.Name)
		resClass := res.Name + "Resource"
		fmt.Fprintf(&w, "  readonly %s: %s\n", slug, resClass)
	}
	w.WriteString("}\n\n")

	// createClient factory
	w.WriteString("export function createClient(request: RequestFn): AdminClient {\n")
	w.WriteString("  const client: Client = { request }\n")
	for _, res := range spec.Resources {
		slug := slugify(res.Name)
		resClass := res.Name + "Resource"
		fmt.Fprintf(&w, "  let _%s: %s | undefined\n", slug, resClass)
	}
	w.WriteString("  return {\n")
	for _, res := range spec.Resources {
		slug := slugify(res.Name)
		resClass := res.Name + "Resource"
		fmt.Fprintf(&w, "    get %s() { return _%s ??= new %s(client) },\n", slug, slug, resClass)
	}
	w.WriteString("  }\n")
	w.WriteString("}\n")

	// Resource classes
	for _, res := range spec.Resources {
		w.WriteString("\n")
		writeTSResource(&w, spec, res)
	}

	writeFile(filepath.Join(outDir, "client_gen.ts"), w.String())
}

func writeTSResource(w *strings.Builder, spec *Spec, res Resource) {
	resClass := res.Name + "Resource"
	fmt.Fprintf(w, "export class %s {\n", resClass)
	w.WriteString("  constructor(private client: Client) {}\n")

	fullBasePath := spec.BasePath + res.Path
	resPathParams := extractPathParams(fullBasePath)

	for _, ep := range res.Endpoints {
		w.WriteString("\n")
		writeTSMethod(w, spec, res, ep, fullBasePath, resPathParams)
	}
	w.WriteString("}\n")
}

func writeTSMethod(w *strings.Builder, spec *Spec, res Resource, ep Endpoint, fullBasePath string, resPathParams []string) {
	epPathParams := extractPathParams(ep.Path)
	allPathParams := make([]string, 0, len(resPathParams)+len(epPathParams))
	allPathParams = append(allPathParams, resPathParams...)
	allPathParams = append(allPathParams, epPathParams...)

	methodName := tsMethodName(ep.Action)
	fullPath := fullBasePath + ep.Path
	fullPath = strings.TrimSuffix(fullPath, "/")
	if fullPath == "" {
		fullPath = "/"
	}

	pt := res.PathParamTypes

	switch {
	case ep.ResponseArray:
		writeTSArrayMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case ep.Pagination == "page":
		writeTSPageListMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case ep.Pagination == "cursor":
		writeTSCursorListMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case len(ep.Query) > 0 && ep.Pagination == "":
		writeTSQueryMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case len(ep.Request) > 0:
		writeTSBodyMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case ep.ResponseType != "":
		writeTSGetMethod(w, methodName, ep, fullPath, allPathParams, pt)
	case len(ep.Response) > 0:
		writeTSToggleMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	default:
		writeTSVoidMethod(w, methodName, ep, fullPath, allPathParams, pt)
	}
}

func tsMethodName(action string) string {
	return camelCase(action)
}

func tsReturnType(ep Endpoint, resName string) string {
	if ep.ResponseArray {
		return ep.ResponseType + "[]"
	}
	if ep.ResponseType != "" && ep.Pagination == "page" {
		return "PaginatedResponse<" + ep.ResponseType + ">"
	}
	if ep.ResponseType != "" && ep.Pagination == "cursor" {
		return "CursorPaginatedResponse<" + ep.ResponseType + ">"
	}
	if ep.ResponseType != "" {
		return ep.ResponseType
	}
	if len(ep.Response) > 0 {
		return tsInlineResponseType(ep.Response)
	}
	return "void"
}

func tsInlineResponseType(resp []string) string {
	var parts []string
	for _, s := range resp {
		f := parseField(s)
		parts = append(parts, f.Name+": "+tsType(f.Type))
	}
	return "{ " + strings.Join(parts, "; ") + " }"
}

func tsPathExpr(fullPath string, allPathParams []string) string {
	if len(allPathParams) == 0 {
		return "'" + fullPath + "'"
	}
	result := fullPath
	for _, p := range allPathParams {
		paramName := tsParamName(p)
		raw := strings.TrimPrefix(p, ":")
		if raw == "nodeId" {
			result = strings.Replace(result, p, "${encodeURIComponent("+paramName+")}", 1)
		} else {
			result = strings.Replace(result, p, "${"+paramName+"}", 1)
		}
	}
	return "`" + result + "`"
}

func tsParamName(pathParam string) string {
	// TS uses JS convention: accountId not accountID
	name := strings.TrimPrefix(pathParam, ":")
	// If already camelCase (e.g., "accountId"), return as-is
	if len(name) > 0 && name[0] >= 'a' && name[0] <= 'z' {
		return name
	}
	return camelCase(name)
}

func tsParams(allPathParams []string, paramTypes map[string]string) string {
	var params []string
	for _, p := range allPathParams {
		raw := strings.TrimPrefix(p, ":")
		t := "number" // default
		if pt, ok := paramTypes[raw]; ok {
			t = tsType(pt)
		}
		params = append(params, tsParamName(p)+": "+t)
	}
	return strings.Join(params, ", ")
}

// POST/PUT with request body
func writeTSBodyMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	reqType := requestTypeName(resName, ep.Action)
	retType := tsReturnType(ep, resName)

	params := tsParams(allPathParams, pt)
	sig := ""
	if params != "" {
		sig = params + ", "
	}
	sig += "req: " + reqType + ", signal?: AbortSignal"

	pathExpr := tsPathExpr(fullPath, allPathParams)

	fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, sig, retType)
	fmt.Fprintf(w, "    return this.client.request('%s', %s, req, signal)\n", ep.Method, pathExpr)
	w.WriteString("  }\n")
}

// GET returning single resource
func writeTSGetMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, pt map[string]string) {
	retType := ep.ResponseType
	params := tsParams(allPathParams, pt)
	sig := params
	if sig != "" {
		sig += ", "
	}
	sig += "signal?: AbortSignal"

	pathExpr := tsPathExpr(fullPath, allPathParams)

	fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, sig, retType)
	fmt.Fprintf(w, "    return this.client.request('GET', %s, undefined, signal)\n", pathExpr)
	w.WriteString("  }\n")
}

// POST/DELETE with response but no body (toggle actions)
func writeTSToggleMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	retType := tsInlineResponseType(ep.Response)
	params := tsParams(allPathParams, pt)
	sig := params
	if sig != "" {
		sig += ", "
	}
	sig += "signal?: AbortSignal"
	pathExpr := tsPathExpr(fullPath, allPathParams)

	fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, sig, retType)
	fmt.Fprintf(w, "    return this.client.request('%s', %s, undefined, signal)\n", ep.Method, pathExpr)
	w.WriteString("  }\n")
}

// No response, no body
func writeTSVoidMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, pt map[string]string) {
	params := tsParams(allPathParams, pt)
	sig := params
	if sig != "" {
		sig += ", "
	}
	sig += "signal?: AbortSignal"
	pathExpr := tsPathExpr(fullPath, allPathParams)

	fmt.Fprintf(w, "  %s(%s): Promise<void> {\n", methodName, sig)
	fmt.Fprintf(w, "    return this.client.request('%s', %s, undefined, signal)\n", ep.Method, pathExpr)
	w.WriteString("  }\n")
}

// GET returning array (with optional query params)
func writeTSArrayMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	retType := tsType(ep.ResponseType) + "[]"
	params := tsParams(allPathParams, pt)

	if len(ep.Query) > 0 {
		// Add query params as individual optional method params
		for _, qs := range ep.Query {
			f := parseField(qs)
			paramName := tsQueryParamName(f.Name)
			if params != "" {
				params += ", "
			}
			opt := "?"
			if f.Required {
				opt = ""
			}
			params += paramName + opt + ": " + tsType(f.Type)
		}
		params += ", signal?: AbortSignal"
		var qsArgs []string
		for _, qs := range ep.Query {
			f := parseField(qs)
			paramName := tsQueryParamName(f.Name)
			qsArgs = append(qsArgs, f.Name+": "+paramName)
		}
		qsCall := "queryString({ " + strings.Join(qsArgs, ", ") + " })"
		pathExpr := tsPathExpr(fullPath, allPathParams)
		pathInner := pathExpr[1 : len(pathExpr)-1]
		fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, params, retType)
		fmt.Fprintf(w, "    return this.client.request('GET', `%s${%s}`, undefined, signal)\n", pathInner, qsCall)
	} else {
		if params != "" {
			params += ", "
		}
		params += "signal?: AbortSignal"
		pathExpr := tsPathExpr(fullPath, allPathParams)
		fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, params, retType)
		fmt.Fprintf(w, "    return this.client.request('GET', %s, undefined, signal)\n", pathExpr)
	}
	w.WriteString("  }\n")
}

// GET with page pagination
func writeTSPageListMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	hasCustomOpts := hasExtraQueryParam(ep.Query)
	retType := "PaginatedResponse<" + ep.ResponseType + ">"

	params := tsParams(allPathParams, pt)

	var optsType string
	var optsOptional string
	if hasCustomOpts {
		optsType = listOptionsTypeName(resName)
		if hasRequiredQueryParam(ep.Query) {
			optsOptional = ""
		} else {
			optsOptional = "?"
		}
	} else {
		optsType = "ListOptions"
		optsOptional = "?"
	}

	sig := ""
	if params != "" {
		sig = params + ", "
	}
	sig += "opts" + optsOptional + ": " + optsType + ", signal?: AbortSignal"

	// Build queryString call
	var qsArgs []string
	for _, qs := range ep.Query {
		f := parseField(qs)
		optRef := "opts"
		if optsOptional == "?" {
			optRef = "opts?"
		}
		qsArgs = append(qsArgs, f.Name+": "+optRef+"."+f.Name)
	}
	qsCall := "queryString({ " + strings.Join(qsArgs, ", ") + " })"

	pathExpr := tsPathExpr(fullPath, allPathParams)
	// For template literal paths with path params, we need to concat
	if len(allPathParams) > 0 {
		fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, sig, retType)
		fmt.Fprintf(w, "    return this.client.request('GET', %s + %s, undefined, signal)\n", pathExpr, qsCall)
	} else {
		// Use template literal
		fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, sig, retType)
		pathStr := fullPath
		fmt.Fprintf(w, "    return this.client.request('GET', `%s${%s}`, undefined, signal)\n", pathStr, qsCall)
	}
	w.WriteString("  }\n")
}

// GET with cursor pagination
func writeTSCursorListMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	optsType := listOptionsTypeName(resName)
	retType := "CursorPaginatedResponse<" + ep.ResponseType + ">"

	params := tsParams(allPathParams, pt)
	sig := ""
	if params != "" {
		sig = params + ", "
	}
	sig += "opts?: " + optsType + ", signal?: AbortSignal"

	// Build queryString call
	var qsArgs []string
	for _, qs := range ep.Query {
		f := parseField(qs)
		qsArgs = append(qsArgs, f.Name+": opts?."+f.Name)
	}
	qsCall := "queryString({\n      " + strings.Join(qsArgs, ",\n      ") + ",\n    })"

	pathExpr := tsPathExpr(fullPath, allPathParams)
	// Strip surrounding quotes/backticks to embed in template literal
	inner := pathExpr[1 : len(pathExpr)-1]

	fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, sig, retType)
	fmt.Fprintf(w, "    return this.client.request('GET', `%s${%s}`, undefined, signal)\n", inner, qsCall)
	w.WriteString("  }\n")
}

// GET with non-pagination query params (like Discover.Meta)
func writeTSQueryMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	retType := tsReturnType(ep, resName)
	params := tsParams(allPathParams, pt)

	// Query params become method parameters (use tsQueryParamName for JS conventions)
	for _, qs := range ep.Query {
		f := parseField(qs)
		paramName := tsQueryParamName(f.Name)
		if params != "" {
			params += ", "
		}
		params += paramName + ": " + tsType(f.Type)
	}
	if params != "" {
		params += ", "
	}
	params += "signal?: AbortSignal"

	// Build queryString
	var qsArgs []string
	for _, qs := range ep.Query {
		f := parseField(qs)
		paramName := tsQueryParamName(f.Name)
		qsArgs = append(qsArgs, f.Name+": "+paramName)
	}
	qsCall := "queryString({ " + strings.Join(qsArgs, ", ") + " })"

	pathExpr := tsPathExpr(fullPath, allPathParams)
	// Strip outer quotes/backticks — we need raw content for template literal with query string
	pathInner := pathExpr[1 : len(pathExpr)-1]

	fmt.Fprintf(w, "  %s(%s): Promise<%s> {\n", methodName, params, retType)
	fmt.Fprintf(w, "    return this.client.request('GET', `%s${%s}`, undefined, signal)\n", pathInner, qsCall)
	w.WriteString("  }\n")
}

// tsQueryParamName converts a query param name to TS convention.
// "access_key_id" -> "accessKeyId" (JS camelCase, no abbreviation uppercasing)
func tsQueryParamName(name string) string {
	// Split on underscores
	parts := strings.Split(name, "_")
	if len(parts) == 1 {
		return name
	}
	var b strings.Builder
	for i, p := range parts {
		if i == 0 {
			b.WriteString(strings.ToLower(p))
		} else {
			b.WriteString(strings.ToUpper(p[:1]) + p[1:])
		}
	}
	return b.String()
}
