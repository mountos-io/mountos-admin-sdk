package main

import (
	"fmt"
	"path/filepath"
	"strings"
)

func generateDocGo(spec *Spec, outDir string) {
	var w strings.Builder

	w.WriteString("<!-- @generated by gen/docgengo.go from api.yaml. DO NOT EDIT. -->\n\n")
	w.WriteString("# mountOS Admin SDK — Go API Reference\n\n")
	w.WriteString("Generated from [api.yaml](../api.yaml). Do not edit.\n\n")
	w.WriteString("- Module: `github.com/mountos-io/mountos-admin-sdk/go`\n")
	fmt.Fprintf(&w, "- Spec version: `%s`\n", spec.Version)
	fmt.Fprintf(&w, "- Base path: `%s`\n\n", spec.BasePath)

	w.WriteString("## Quick Start\n\n")
	w.WriteString("```go\n")
	w.WriteString("import sdk \"github.com/mountos-io/mountos-admin-sdk/go\"\n\n")
	w.WriteString("client, err := sdk.NewClient(sdk.Config{\n")
	w.WriteString("    BaseURL:    \"https://appserv.example.com\",\n")
	w.WriteString("    PrivateKey: \"<base64 ED25519 key>\",\n")
	w.WriteString("})\n")
	w.WriteString("```\n\n")

	w.WriteString("## Response Envelope\n\n")
	w.WriteString("SDK methods return the unwrapped `data` payload as a typed value plus `error` (set on `status=\"failure\"` or transport failure).\n\n")
	w.WriteString("```go\n")
	w.WriteString("type StandardResponse[T any] struct {\n")
	w.WriteString("    Status    string `json:\"status\"`\n")
	w.WriteString("    Message   string `json:\"message\"`\n")
	w.WriteString("    Data      T      `json:\"data,omitempty\"`\n")
	w.WriteString("    ErrorCode int    `json:\"errorCode,omitempty\"`\n")
	w.WriteString("}\n\n")
	w.WriteString("type PaginatedResponse[T any] struct {\n")
	w.WriteString("    Items      []T            `json:\"items\"`\n")
	w.WriteString("    Pagination PaginationMeta `json:\"pagination\"`\n")
	w.WriteString("}\n\n")
	w.WriteString("type CursorResponse[T any] struct {\n")
	w.WriteString("    Items      []T    `json:\"items\"`\n")
	w.WriteString("    NextCursor *int64 `json:\"nextCursor\"`\n")
	w.WriteString("}\n")
	w.WriteString("```\n\n")

	w.WriteString("## Error Codes\n\n")
	w.WriteString("| Code  | Name                          |\n")
	w.WriteString("|-------|-------------------------------|\n")
	for _, ec := range spec.ErrorCodes {
		fmt.Fprintf(&w, "| %d | %s |\n", ec.Code, ec.Name)
	}
	w.WriteString("\n")

	// Enums
	if len(spec.Enums) > 0 {
		w.WriteString("## Enums\n\n")
		for _, name := range sortedKeys(spec.Enums) {
			values := spec.Enums[name]
			fmt.Fprintf(&w, "### `%s`\n\n", name)
			w.WriteString("```go\n")
			fmt.Fprintf(&w, "type %s string\n\n", name)
			w.WriteString("const (\n")
			for _, v := range values {
				fmt.Fprintf(&w, "    %s%s %s = %q\n", name, pascalCase(v), name, v)
			}
			w.WriteString(")\n```\n\n")
		}
	}

	// Types
	if len(spec.Types) > 0 {
		w.WriteString("## Types\n\n")
		for _, name := range sortedKeys(spec.Types) {
			fields := spec.Types[name]
			fmt.Fprintf(&w, "### `%s`\n\n", name)
			w.WriteString("```go\n")
			fmt.Fprintf(&w, "type %s struct {\n", name)
			for _, fs := range fields {
				f := parseField(fs)
				tag := f.Name
				if f.Optional {
					tag += ",omitempty"
				}
				fmt.Fprintf(&w, "    %-24s %-24s `json:%q`\n",
					goFieldName(f.Name), goDocType(f.Type, f.Optional), tag)
			}
			w.WriteString("}\n```\n\n")
		}
	}

	// Resources
	w.WriteString("## Resources\n\n")
	for _, res := range spec.Resources {
		fmt.Fprintf(&w, "### %s\n\n", res.Name)
		fmt.Fprintf(&w, "Accessor: `client.%s`\n\n", res.Name)

		fullBase := spec.BasePath + res.Path
		for _, ep := range res.Endpoints {
			fullPath := strings.TrimSuffix(fullBase+ep.Path, "/")
			actionFn := pascalCase(ep.Action)

			fmt.Fprintf(&w, "#### `%s` — %s %s\n\n", actionFn, ep.Method, fullPath)

			args := []string{"ctx context.Context"}
			for _, p := range extractPathParams(fullPath) {
				name := goParamName(p)
				typ := "int64"
				if res.PathParamTypes != nil {
					if t, ok := res.PathParamTypes[strings.TrimPrefix(p, ":")]; ok {
						typ = goDocType(t, false)
					}
				}
				args = append(args, name+" "+typ)
			}

			reqType := ""
			if len(ep.Request) > 0 {
				reqType = inferGoRequestType(res, ep, "Request")
				args = append(args, "req *"+reqType)
			} else if len(ep.Query) > 0 {
				reqType = inferGoRequestType(res, ep, "Options")
				args = append(args, "opts *"+reqType)
			}

			retType := goReturnType(ep)

			w.WriteString("```go\n")
			fmt.Fprintf(&w, "func (s *%sService) %s(%s) (%s, error)\n", res.Name, actionFn, strings.Join(args, ", "), retType)
			w.WriteString("```\n\n")

			if len(ep.Request) > 0 {
				w.WriteString("Request body:\n\n")
				w.WriteString("```go\n")
				fmt.Fprintf(&w, "type %s struct {\n", reqType)
				for _, fs := range ep.Request {
					f := parseField(fs)
					tag := f.Name
					if !f.Required {
						tag += ",omitempty"
					}
					fmt.Fprintf(&w, "    %-24s %-24s `json:%q`\n",
						goFieldName(f.Name), goDocType(f.Type, !f.Required), tag)
				}
				w.WriteString("}\n```\n\n")
			}
			if len(ep.Query) > 0 {
				w.WriteString("Query params:\n\n")
				w.WriteString("```go\n")
				fmt.Fprintf(&w, "type %s struct {\n", reqType)
				for _, fs := range ep.Query {
					f := parseField(fs)
					comment := ""
					if f.Default != "" {
						comment = " // default: " + f.Default
					}
					fmt.Fprintf(&w, "    %-20s %-12s `url:%q`%s\n",
						goFieldName(f.Name), goDocType(f.Type, false), f.Name, comment)
				}
				w.WriteString("}\n```\n\n")
			}
		}
	}

	writeFile(filepath.Join(outDir, "go.md"), w.String())
}

func goDocType(t string, ptr bool) string {
	if strings.HasSuffix(t, "[]") {
		return "[]" + goDocType(strings.TrimSuffix(t, "[]"), false)
	}
	base := ""
	switch t {
	case "string", "datetime":
		base = "string"
	case "int":
		base = "int"
	case "int32":
		base = "int32"
	case "int64":
		base = "int64"
	case "bool":
		base = "bool"
	case "object":
		base = "map[string]any"
	case "json":
		base = "json.RawMessage"
	default:
		base = t
	}
	if ptr && base != "" && base != "string" && !strings.HasPrefix(base, "[]") && !strings.HasPrefix(base, "map[") {
		// keep simple — only pointer-wrap scalar struct-ish enum names
	}
	return base
}

func goReturnType(ep Endpoint) string {
	if ep.ResponseType != "" {
		base := ep.ResponseType
		if ep.ResponseArray {
			return "[]" + base
		}
		switch ep.Pagination {
		case "page":
			return "*PaginatedResponse[" + base + "]"
		case "cursor":
			return "*CursorResponse[" + base + "]"
		}
		return "*" + base
	}
	if len(ep.Response) > 0 {
		// Heuristic: single id field → *IDResponse, otherwise anonymous struct.
		if len(ep.Response) == 1 {
			f := parseField(ep.Response[0])
			if f.Name == "id" && f.Type == "int64" {
				return "*IDResponse"
			}
		}
		var parts []string
		for _, rs := range ep.Response {
			f := parseField(rs)
			parts = append(parts, goFieldName(f.Name)+" "+goDocType(f.Type, false))
		}
		return "*struct { " + strings.Join(parts, "; ") + " }"
	}
	return "*StandardResponse[any]"
}

// inferGoRequestType picks a stable per-endpoint type name (Action + ResourceSingular + suffix).
func inferGoRequestType(res Resource, ep Endpoint, suffix string) string {
	singular := strings.TrimSuffix(res.Name, "s")
	if strings.HasSuffix(res.Name, "ies") {
		singular = strings.TrimSuffix(res.Name, "ies") + "y"
	}
	return pascalCase(ep.Action) + singular + suffix
}
