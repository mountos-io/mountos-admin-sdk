package main

import (
	"fmt"
	"path/filepath"
	"sort"
	"strings"
)

func generateDocTS(spec *Spec, outDir string) {
	var w strings.Builder

	w.WriteString("<!-- @generated by gen/docgents.go from api.yaml. DO NOT EDIT. -->\n\n")
	w.WriteString("# mountOS Admin SDK - TypeScript API Reference\n\n")
	w.WriteString("Generated from [api.yaml](../api.yaml). Do not edit.\n\n")
	w.WriteString("- Package: `@mountos-io/admin-sdk`\n")
	fmt.Fprintf(&w, "- Spec version: `%s`\n", spec.Version)
	fmt.Fprintf(&w, "- Base path: `%s`\n\n", spec.BasePath)

	w.WriteString("## Quick Start\n\n")
	w.WriteString("```typescript\n")
	w.WriteString("import { createServerClient } from \"@mountos-io/admin-sdk\";\n\n")
	w.WriteString("const client = createServerClient({\n")
	w.WriteString("  baseUrl: \"https://appserv.example.com\",\n")
	w.WriteString("  privateKey: \"<base64 ED25519 key>\",\n")
	w.WriteString("});\n")
	w.WriteString("```\n\n")
	w.WriteString("Browser apps use `createClient(request)` with a custom `RequestFn`. See `README.md`.\n\n")

	w.WriteString("## Response Envelope\n\n")
	w.WriteString("All SDK methods return the unwrapped `data` payload and throw `MountOSError` on `status=\"failure\"`.\n\n")
	w.WriteString("```typescript\n")
	w.WriteString("type StandardResponse<T> = {\n")
	w.WriteString("  status: \"success\" | \"failure\";\n")
	w.WriteString("  message: string;\n")
	w.WriteString("  data?: T;\n")
	w.WriteString("  errorCode?: number;\n")
	w.WriteString("};\n\n")
	w.WriteString("type PaginatedResponse<T>       = { items: T[]; pagination: PaginationMeta };\n")
	w.WriteString("type CursorPaginatedResponse<T> = { items: T[]; nextCursor: number | null };\n")
	w.WriteString("type PaginationMeta             = { page: number; limit: number; total: number; totalPages: number };\n")
	w.WriteString("```\n\n")

	w.WriteString("## Error Codes\n\n")
	w.WriteString("| Code  | Name                          |\n")
	w.WriteString("|-------|-------------------------------|\n")
	for _, ec := range spec.ErrorCodes {
		fmt.Fprintf(&w, "| %d | %s |\n", ec.Code, ec.Name)
	}
	w.WriteString("\n")

	// Enums
	if len(spec.Enums) > 0 {
		w.WriteString("## Enums\n\n")
		names := sortedKeys(spec.Enums)
		for _, name := range names {
			values := spec.Enums[name]
			fmt.Fprintf(&w, "### `%s`\n\n", name)
			w.WriteString("```typescript\n")
			fmt.Fprintf(&w, "type %s = ", name)
			parts := make([]string, len(values))
			for i, v := range values {
				parts[i] = "\"" + v + "\""
			}
			w.WriteString(strings.Join(parts, " | "))
			w.WriteString(";\n")
			w.WriteString("```\n\n")
		}
	}

	// Types
	if len(spec.Types) > 0 {
		w.WriteString("## Types\n\n")
		names := sortedKeys(spec.Types)
		for _, name := range names {
			fields := spec.Types[name]
			fmt.Fprintf(&w, "### `%s`\n\n", name)
			w.WriteString("```typescript\n")
			fmt.Fprintf(&w, "interface %s {\n", name)
			for _, fs := range fields {
				f := parseField(fs)
				suffix := ""
				if f.Optional {
					suffix = "?"
				}
				fmt.Fprintf(&w, "  %s%s: %s;\n", f.Name, suffix, tsType(f.Type))
			}
			w.WriteString("}\n```\n\n")
		}
	}

	// Resources
	w.WriteString("## Resources\n\n")
	for _, res := range spec.Resources {
		fmt.Fprintf(&w, "### %s\n\n", res.Name)
		accessor := camelCase(res.Name)
		fmt.Fprintf(&w, "Accessor: `client.%s`\n\n", accessor)

		fullBase := spec.BasePath + res.Path
		for _, ep := range res.Endpoints {
			fullPath := strings.TrimSuffix(fullBase+ep.Path, "/")
			actionFn := camelCase(ep.Action)

			fmt.Fprintf(&w, "#### `%s` - %s %s\n\n", actionFn, ep.Method, fullPath)

			// Reuses the real tsParams/tsQueryParamName/tsType/tsReturnType
			// (tsgen.go) and mirrors writeTSMethod's dispatch case-for-case
			// (same conditions, same order) so the documented signature can
			// never drift from what client_gen.ts actually emits: named
			// request/opts types where the real method takes one, individual
			// positional params where it doesn't, no arg at all where the
			// real method has neither.
			pathParams := extractPathParams(fullPath)
			pathArgs := tsParams(pathParams, res.PathParamTypes)

			var sig, retType, reqSection, querySection string

			switch {
			case ep.ResponseArray:
				params := pathArgs
				for _, qs := range ep.Query {
					f := parseField(qs)
					paramName := tsQueryParamName(f.Name)
					if params != "" {
						params += ", "
					}
					opt := "?"
					if f.Required {
						opt = ""
					}
					params += paramName + opt + ": " + tsType(f.Type)
				}
				if params != "" {
					params += ", "
				}
				sig = params + "signal?: AbortSignal"
				retType = tsType(ep.ResponseType) + "[]"
				if len(ep.Query) > 0 {
					querySection = tsBlockObject(ep.Query)
				}

			case ep.Pagination == "page":
				optsType := "ListOptions"
				printOpts := false
				if hasExtraQueryParam(ep.Query) {
					optsType = listOptionsTypeName(res.Name)
					printOpts = true
				}
				optsOptional := "?"
				if hasRequiredQueryParam(ep.Query) {
					optsOptional = ""
				}
				params := pathArgs
				if params != "" {
					params += ", "
				}
				sig = params + "opts" + optsOptional + ": " + optsType + ", signal?: AbortSignal"
				retType = "PaginatedResponse<" + ep.ResponseType + ">"
				if printOpts {
					querySection = tsBlockObject(ep.Query)
				}

			case ep.Pagination == "cursor":
				optsType := listOptionsTypeName(res.Name)
				optsOptional := "?"
				if hasRequiredQueryParam(ep.Query) {
					optsOptional = ""
				}
				params := pathArgs
				if params != "" {
					params += ", "
				}
				sig = params + "opts" + optsOptional + ": " + optsType + ", signal?: AbortSignal"
				retType = "CursorPaginatedResponse<" + ep.ResponseType + ">"
				querySection = tsBlockObject(ep.Query)

			case len(ep.Query) > 0 && ep.Pagination == "":
				params := pathArgs
				for _, qs := range ep.Query {
					f := parseField(qs)
					paramName := tsQueryParamName(f.Name)
					if params != "" {
						params += ", "
					}
					opt := "?"
					if f.Required {
						opt = ""
					}
					params += paramName + opt + ": " + tsType(f.Type)
				}
				if params != "" {
					params += ", "
				}
				sig = params + "signal?: AbortSignal"
				retType = tsReturnType(ep)
				querySection = tsBlockObject(ep.Query)

			case len(ep.Request) > 0:
				reqType := requestTypeName(res.Name, ep.Action)
				params := pathArgs
				if params != "" {
					params += ", "
				}
				sig = params + "req: " + reqType + ", signal?: AbortSignal"
				retType = tsReturnType(ep)
				reqSection = tsBlockObject(ep.Request)

			case ep.ResponseType != "":
				params := pathArgs
				if params != "" {
					params += ", "
				}
				sig = params + "signal?: AbortSignal"
				retType = ep.ResponseType

			case len(ep.Response) > 0:
				params := pathArgs
				if params != "" {
					params += ", "
				}
				sig = params + "signal?: AbortSignal"
				// Inline, matching writeTSToggleMethod/tsInlineResponseType --
				// TS's return type already shows the shape, unlike Go/Rust
				// which reference a named type and need a separate section.
				retType = tsInlineResponseType(ep.Response)

			default:
				params := pathArgs
				if params != "" {
					params += ", "
				}
				sig = params + "signal?: AbortSignal"
				retType = "void"
			}

			w.WriteString("```typescript\n")
			fmt.Fprintf(&w, "client.%s.%s(%s): Promise<%s>;\n", accessor, actionFn, sig, retType)
			w.WriteString("```\n\n")

			if reqSection != "" {
				w.WriteString("Request body:\n\n")
				w.WriteString("```typescript\n")
				w.WriteString(reqSection)
				w.WriteString("```\n\n")
			}
			if querySection != "" {
				w.WriteString("Query params:\n\n")
				w.WriteString("```typescript\n")
				w.WriteString(querySection)
				w.WriteString("```\n\n")
			}
		}
	}

	writeFile(filepath.Join(outDir, "ts.md"), w.String())
}

func tsBlockObject(fields []string) string {
	var sb strings.Builder
	sb.WriteString("{\n")
	for _, fs := range fields {
		f := parseField(fs)
		suffix := ""
		if !f.Required {
			suffix = "?"
		}
		comment := ""
		if f.Default != "" {
			comment = "  // default: " + f.Default
		}
		fmt.Fprintf(&sb, "  %s%s: %s;%s\n", f.Name, suffix, tsType(f.Type), comment)
	}
	sb.WriteString("}\n")
	return sb.String()
}

func sortedKeys[V any](m map[string]V) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
