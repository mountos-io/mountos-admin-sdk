package main

import (
	"fmt"
	"path/filepath"
	"sort"
	"strings"
)

func generateDocTS(spec *Spec, outDir string) {
	var w strings.Builder

	w.WriteString("<!-- @generated by gen/docgents.go from api.yaml. DO NOT EDIT. -->\n\n")
	w.WriteString("# mountOS Admin SDK — TypeScript API Reference\n\n")
	w.WriteString("Generated from [api.yaml](../api.yaml). Do not edit.\n\n")
	w.WriteString("- Package: `@mountos-io/admin-sdk`\n")
	fmt.Fprintf(&w, "- Spec version: `%s`\n", spec.Version)
	fmt.Fprintf(&w, "- Base path: `%s`\n\n", spec.BasePath)

	w.WriteString("## Quick Start\n\n")
	w.WriteString("```typescript\n")
	w.WriteString("import { MountOSAdmin } from \"@mountos-io/admin-sdk\";\n\n")
	w.WriteString("const client = new MountOSAdmin({\n")
	w.WriteString("  baseUrl: \"https://appserv.example.com\",\n")
	w.WriteString("  privateKey: \"<base64 ED25519 key>\",\n")
	w.WriteString("});\n")
	w.WriteString("```\n\n")

	w.WriteString("## Response Envelope\n\n")
	w.WriteString("All SDK methods return the unwrapped `data` payload and throw `MountOSAdminError` on `status=\"failure\"`.\n\n")
	w.WriteString("```typescript\n")
	w.WriteString("type StandardResponse<T> = {\n")
	w.WriteString("  status: \"success\" | \"failure\";\n")
	w.WriteString("  message: string;\n")
	w.WriteString("  data?: T;\n")
	w.WriteString("  errorCode?: number;\n")
	w.WriteString("};\n\n")
	w.WriteString("type PaginatedResponse<T> = { items: T[]; pagination: PaginationMeta };\n")
	w.WriteString("type CursorResponse<T>   = { items: T[]; nextCursor: number | null };\n")
	w.WriteString("type PaginationMeta      = { page: number; limit: number; total: number; totalPages: number };\n")
	w.WriteString("```\n\n")

	w.WriteString("## Error Codes\n\n")
	w.WriteString("| Code  | Name                          |\n")
	w.WriteString("|-------|-------------------------------|\n")
	for _, ec := range spec.ErrorCodes {
		fmt.Fprintf(&w, "| %d | %s |\n", ec.Code, ec.Name)
	}
	w.WriteString("\n")

	// Enums
	if len(spec.Enums) > 0 {
		w.WriteString("## Enums\n\n")
		names := sortedKeys(spec.Enums)
		for _, name := range names {
			values := spec.Enums[name]
			fmt.Fprintf(&w, "### `%s`\n\n", name)
			w.WriteString("```typescript\n")
			fmt.Fprintf(&w, "type %s = ", name)
			parts := make([]string, len(values))
			for i, v := range values {
				parts[i] = "\"" + v + "\""
			}
			w.WriteString(strings.Join(parts, " | "))
			w.WriteString(";\n")
			w.WriteString("```\n\n")
		}
	}

	// Types
	if len(spec.Types) > 0 {
		w.WriteString("## Types\n\n")
		names := sortedKeys(spec.Types)
		for _, name := range names {
			fields := spec.Types[name]
			fmt.Fprintf(&w, "### `%s`\n\n", name)
			w.WriteString("```typescript\n")
			fmt.Fprintf(&w, "interface %s {\n", name)
			for _, fs := range fields {
				f := parseField(fs)
				suffix := ""
				if f.Optional {
					suffix = "?"
				}
				fmt.Fprintf(&w, "  %s%s: %s;\n", f.Name, suffix, tsDocType(f.Type))
			}
			w.WriteString("}\n```\n\n")
		}
	}

	// Resources
	w.WriteString("## Resources\n\n")
	for _, res := range spec.Resources {
		fmt.Fprintf(&w, "### %s\n\n", res.Name)
		accessor := camelCase(res.Name)
		fmt.Fprintf(&w, "Accessor: `client.%s`\n\n", accessor)

		fullBase := spec.BasePath + res.Path
		for _, ep := range res.Endpoints {
			fullPath := strings.TrimSuffix(fullBase+ep.Path, "/")
			actionFn := camelCase(ep.Action)

			fmt.Fprintf(&w, "#### `%s` — %s %s\n\n", actionFn, ep.Method, fullPath)

			// Path params
			pathParams := extractPathParams(fullPath)
			pathArgs := make([]string, 0, len(pathParams))
			for _, p := range pathParams {
				name := camelCase(strings.TrimPrefix(p, ":"))
				typ := "number"
				if res.PathParamTypes != nil {
					if t, ok := res.PathParamTypes[strings.TrimPrefix(p, ":")]; ok {
						typ = tsDocType(t)
					}
				}
				pathArgs = append(pathArgs, name+": "+typ)
			}

			// Body / query arg
			bodyArg := ""
			if len(ep.Request) > 0 {
				bodyArg = "body: " + tsInlineObject(ep.Request, "  ")
			} else if len(ep.Query) > 0 {
				bodyArg = "params: " + tsInlineObject(ep.Query, "  ")
			}

			// Return type
			retType := tsDocReturnType(ep)

			// Signature
			w.WriteString("```typescript\n")
			args := pathArgs
			if bodyArg != "" {
				args = append(args, bodyArg)
			}
			fmt.Fprintf(&w, "client.%s.%s(%s): Promise<%s>;\n", accessor, actionFn, strings.Join(args, ", "), retType)
			w.WriteString("```\n\n")

			if len(ep.Request) > 0 {
				w.WriteString("Request body:\n\n")
				w.WriteString("```typescript\n")
				w.WriteString(tsBlockObject(ep.Request))
				w.WriteString("```\n\n")
			}
			if len(ep.Query) > 0 {
				w.WriteString("Query params:\n\n")
				w.WriteString("```typescript\n")
				w.WriteString(tsBlockObject(ep.Query))
				w.WriteString("```\n\n")
			}
		}
	}

	writeFile(filepath.Join(outDir, "ts.md"), w.String())
}

func tsDocType(t string) string {
	if strings.HasSuffix(t, "[]") {
		return tsDocType(strings.TrimSuffix(t, "[]")) + "[]"
	}
	switch t {
	case "string", "datetime":
		return "string"
	case "int", "int32", "int64":
		return "number"
	case "bool":
		return "boolean"
	case "object", "json":
		return "Record<string, unknown>"
	default:
		return t
	}
}

func tsInlineObject(fields []string, indent string) string {
	var sb strings.Builder
	sb.WriteString("{\n")
	for _, fs := range fields {
		f := parseField(fs)
		suffix := ""
		if !f.Required {
			suffix = "?"
		}
		fmt.Fprintf(&sb, "%s  %s%s: %s;\n", indent, f.Name, suffix, tsDocType(f.Type))
	}
	fmt.Fprintf(&sb, "%s}", indent)
	return sb.String()
}

func tsBlockObject(fields []string) string {
	var sb strings.Builder
	sb.WriteString("{\n")
	for _, fs := range fields {
		f := parseField(fs)
		suffix := ""
		if !f.Required {
			suffix = "?"
		}
		comment := ""
		if f.Default != "" {
			comment = "  // default: " + f.Default
		}
		fmt.Fprintf(&sb, "  %s%s: %s;%s\n", f.Name, suffix, tsDocType(f.Type), comment)
	}
	sb.WriteString("}\n")
	return sb.String()
}

func tsDocReturnType(ep Endpoint) string {
	if ep.ResponseType != "" {
		base := ep.ResponseType
		if ep.ResponseArray {
			return base + "[]"
		}
		switch ep.Pagination {
		case "page":
			return "PaginatedResponse<" + base + ">"
		case "cursor":
			return "CursorResponse<" + base + ">"
		}
		return base
	}
	if len(ep.Response) > 0 {
		var parts []string
		for _, rs := range ep.Response {
			f := parseField(rs)
			parts = append(parts, f.Name+": "+tsDocType(f.Type))
		}
		return "{ " + strings.Join(parts, "; ") + " }"
	}
	return "void"
}

func sortedKeys[V any](m map[string]V) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
