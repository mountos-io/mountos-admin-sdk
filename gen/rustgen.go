package main

import (
	"fmt"
	"path/filepath"
	"strings"
)

func generateRust(spec *Spec, outDir string) {
	generateRustTypes(spec, outDir)
	generateRustClient(spec, outDir)
}

// rustType maps a spec type to its Rust equivalent.
func rustType(specType string) string {
	if strings.HasSuffix(specType, "[]") {
		return "Vec<" + rustType(specType[:len(specType)-2]) + ">"
	}
	switch specType {
	case "string", "datetime":
		return "String"
	case "int64":
		return "i64"
	case "int32":
		return "i32"
	case "int":
		return "i64"
	case "bool":
		return "bool"
	case "float", "float64":
		return "f64"
	case "object", "json":
		return "serde_json::Value"
	default:
		// Named type: model struct or string enum alias.
		return specType
	}
}

// rustParamType returns the Rust parameter type for a :path param.
// String params are borrowed (&str); numeric params are owned (i64).
func rustParamType(paramRaw string, paramTypes map[string]string) string {
	if t, ok := paramTypes[paramRaw]; ok && t == "string" {
		return "&str"
	}
	if t, ok := paramTypes[paramRaw]; ok {
		return rustType(t)
	}
	return "i64"
}

func generateRustTypes(spec *Spec, outDir string) {
	var w strings.Builder
	w.WriteString("// Code generated by gen; DO NOT EDIT.\n\n")
	w.WriteString("use serde::{Deserialize, Serialize};\n\n")

	// Config
	w.WriteString("/// Client configuration.\n")
	w.WriteString("#[derive(Debug, Clone, Default)]\n")
	w.WriteString("pub struct Config {\n")
	w.WriteString("    /// Base URL of the appserv provider API, e.g. `https://appserv.example.com`.\n")
	w.WriteString("    pub base_url: String,\n")
	w.WriteString("    /// Base64-encoded ED25519 private key (32-byte seed or 64-byte seed+public key).\n")
	w.WriteString("    pub private_key: String,\n")
	w.WriteString("    /// Optional dashboard operator context, signed into each request.\n")
	w.WriteString("    pub dashboard_user: Option<DashboardUser>,\n")
	w.WriteString("    /// Dedicated HMAC secret for the `X-MountOS-Dashboard-User` header (appserv\n")
	w.WriteString("    /// `DASHBOARD_USER_HMAC_KEY`); required when `dashboard_user` is set.\n")
	w.WriteString("    pub dashboard_hmac_key: Option<String>,\n")
	w.WriteString("}\n\n")

	// ListOptions
	w.WriteString("/// Page-based pagination options.\n")
	w.WriteString("#[derive(Debug, Clone, Default)]\n")
	w.WriteString("pub struct ListOptions {\n")
	w.WriteString("    pub page: Option<i64>,\n")
	w.WriteString("    pub limit: Option<i64>,\n")
	w.WriteString("}\n\n")

	// PaginationMeta
	w.WriteString("/// Pagination metadata returned by page-based list endpoints.\n")
	w.WriteString("#[derive(Debug, Clone, Deserialize)]\n")
	w.WriteString("pub struct PaginationMeta {\n")
	w.WriteString("    pub page: i64,\n")
	w.WriteString("    pub limit: i64,\n")
	w.WriteString("    pub total: i64,\n")
	w.WriteString("    #[serde(rename = \"totalPages\")]\n")
	w.WriteString("    pub total_pages: i64,\n")
	w.WriteString("}\n\n")

	// PaginatedResponse
	w.WriteString("/// Page-based list response.\n")
	w.WriteString("#[derive(Debug, Clone, Deserialize)]\n")
	w.WriteString("pub struct PaginatedResponse<T> {\n")
	w.WriteString("    pub items: Vec<T>,\n")
	w.WriteString("    pub pagination: PaginationMeta,\n")
	w.WriteString("}\n\n")

	// CursorPaginatedResponse
	w.WriteString("/// Cursor-based list response.\n")
	w.WriteString("#[derive(Debug, Clone, Deserialize)]\n")
	w.WriteString("pub struct CursorPaginatedResponse<T> {\n")
	w.WriteString("    pub items: Vec<T>,\n")
	w.WriteString("    #[serde(rename = \"nextCursor\")]\n")
	w.WriteString("    pub next_cursor: Option<i64>,\n")
	w.WriteString("}\n\n")

	// IdResponse
	w.WriteString("/// Identifier returned by create/edit/toggle endpoints.\n")
	w.WriteString("#[derive(Debug, Clone, Deserialize)]\n")
	w.WriteString("pub struct IdResponse {\n")
	w.WriteString("    pub id: i64,\n")
	w.WriteString("}\n")

	// Enum string aliases + value constants.
	for _, name := range orderEnums(spec) {
		values := spec.Enums[name]
		fmt.Fprintf(&w, "\n/// `%s` values accepted/returned on the wire.\n", name)
		fmt.Fprintf(&w, "pub type %s = String;\n", name)
		for _, v := range values {
			constName := screamingSnake(name) + "_" + screamingSnake(v)
			fmt.Fprintf(&w, "pub const %s: &str = %q;\n", constName, v)
		}
	}

	// Model types.
	for _, typeName := range orderTypes(spec) {
		fields, ok := spec.Types[typeName]
		if !ok {
			continue
		}
		w.WriteString("\n")
		writeRustStruct(&w, typeName, fields, false)
	}

	// Resource request/response/list-option types.
	for _, res := range spec.Resources {
		fmt.Fprintf(&w, "\n// %s\n", res.Name)
		for _, ep := range res.Endpoints {
			if len(ep.Request) > 0 {
				w.WriteString("\n")
				writeRustStruct(&w, requestTypeName(res.Name, ep.Action), ep.Request, true)
			}
			if len(ep.Response) > 0 && !responseIsIDOnly(ep.Response) {
				w.WriteString("\n")
				writeRustStruct(&w, customResponseTypeName(res.Name, ep.Action), ep.Response, false)
			}
		}
		for _, ep := range res.Endpoints {
			if ep.Pagination == "cursor" || (ep.Pagination == "page" && hasExtraQueryParam(ep.Query)) {
				w.WriteString("\n")
				writeRustListOptions(&w, listOptionsTypeName(res.Name), ep.Query)
			}
		}
	}

	writeFile(filepath.Join(outDir, "types_gen.rs"), w.String())
}

// writeRustStruct emits a serde struct. Request structs serialize optional
// fields with skip_serializing_if; model structs do the same so optional
// fields are omitted (matching the Go SDK's omitempty).
func writeRustStruct(w *strings.Builder, name string, fields []string, isRequest bool) {
	if isRequest {
		w.WriteString("#[derive(Debug, Clone, Serialize)]\n")
	} else {
		w.WriteString("#[derive(Debug, Clone, Serialize, Deserialize)]\n")
	}
	fmt.Fprintf(w, "pub struct %s {\n", name)
	for _, s := range fields {
		f := parseField(s)
		optional := f.Optional
		if isRequest {
			optional = !f.Required
		}
		writeRustField(w, f, optional)
	}
	w.WriteString("}\n")
}

func writeRustField(w *strings.Builder, f Field, optional bool) {
	rustName := rustFieldName(f.Name)
	ty := rustType(f.Type)
	if optional {
		ty = "Option<" + ty + ">"
	}

	var attrs []string
	if strings.TrimPrefix(rustName, "r#") != f.Name {
		attrs = append(attrs, fmt.Sprintf("rename = %q", f.Name))
	}
	if optional {
		attrs = append(attrs, "skip_serializing_if = \"Option::is_none\"")
	}
	if len(attrs) > 0 {
		fmt.Fprintf(w, "    #[serde(%s)]\n", strings.Join(attrs, ", "))
	}
	fmt.Fprintf(w, "    pub %s: %s,\n", rustName, ty)
}

// writeRustListOptions emits a list-options struct. page/limit/cursor and any
// optional query params are Option<T>; required params are owned values.
func writeRustListOptions(w *strings.Builder, name string, query []string) {
	w.WriteString("#[derive(Debug, Clone, Default)]\n")
	fmt.Fprintf(w, "pub struct %s {\n", name)
	for _, s := range query {
		f := parseField(s)
		ty := rustType(f.Type)
		if !f.Required {
			ty = "Option<" + ty + ">"
		}
		fmt.Fprintf(w, "    pub %s: %s,\n", rustFieldName(f.Name), ty)
	}
	w.WriteString("}\n")
}

func generateRustClient(spec *Spec, outDir string) {
	var w strings.Builder
	w.WriteString("// Code generated by gen; DO NOT EDIT.\n\n")
	w.WriteString("use std::sync::Arc;\n\n")
	w.WriteString("use crate::errors::Error;\n")
	w.WriteString("use crate::http::ClientInner;\n")
	w.WriteString("use crate::types_gen::*;\n\n")

	// Client struct.
	w.WriteString("/// mountOS Admin API client. Construct with [`Client::new`].\n")
	w.WriteString("pub struct Client {\n")
	for _, res := range spec.Resources {
		fmt.Fprintf(&w, "    pub %s: %sService,\n", snakeCase(res.Name), res.Name)
	}
	w.WriteString("}\n\n")

	// Constructor.
	w.WriteString("impl Client {\n")
	w.WriteString("    /// Create a client from configuration. Fails if the private key is invalid.\n")
	w.WriteString("    pub fn new(config: Config) -> Result<Self, Error> {\n")
	w.WriteString("        let inner = Arc::new(ClientInner::new(config)?);\n")
	w.WriteString("        Ok(Self {\n")
	for _, res := range spec.Resources {
		fmt.Fprintf(&w, "            %s: %sService { inner: Arc::clone(&inner) },\n", snakeCase(res.Name), res.Name)
	}
	w.WriteString("        })\n")
	w.WriteString("    }\n")
	w.WriteString("}\n")

	for _, res := range spec.Resources {
		w.WriteString("\n")
		writeRustResource(&w, spec, res)
	}

	writeFile(filepath.Join(outDir, "client_gen.rs"), w.String())
}

func writeRustResource(w *strings.Builder, spec *Spec, res Resource) {
	svcType := res.Name + "Service"
	fmt.Fprintf(w, "/// Operations on the `%s` resource.\n", res.Name)
	fmt.Fprintf(w, "pub struct %s {\n", svcType)
	w.WriteString("    inner: Arc<ClientInner>,\n")
	w.WriteString("}\n\n")
	fmt.Fprintf(w, "impl %s {\n", svcType)

	fullBasePath := spec.BasePath + res.Path
	resPathParams := extractPathParams(fullBasePath)

	for i, ep := range res.Endpoints {
		if i > 0 {
			w.WriteString("\n")
		}
		writeRustMethod(w, res, ep, fullBasePath, resPathParams)
	}
	w.WriteString("}\n")
}

func writeRustMethod(w *strings.Builder, res Resource, ep Endpoint, fullBasePath string, resPathParams []string) {
	epPathParams := extractPathParams(ep.Path)
	allPathParams := append(append([]string{}, resPathParams...), epPathParams...)

	methodName := rustFieldName(ep.Action)
	fullPath := strings.TrimSuffix(fullBasePath+ep.Path, "/")
	if fullPath == "" {
		fullPath = "/"
	}
	pt := res.PathParamTypes

	switch {
	case ep.ResponseArray:
		writeRustArrayMethod(w, methodName, ep, fullPath, allPathParams, pt)
	case ep.Pagination == "page":
		writeRustPageListMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case ep.Pagination == "cursor":
		writeRustCursorListMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case len(ep.Query) > 0:
		writeRustQueryMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case len(ep.Request) > 0:
		writeRustBodyMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	case ep.ResponseType != "":
		writeRustGetMethod(w, methodName, ep, fullPath, allPathParams, pt)
	case len(ep.Response) > 0:
		writeRustToggleMethod(w, methodName, ep, fullPath, allPathParams, res.Name, pt)
	default:
		writeRustVoidMethod(w, methodName, ep, fullPath, allPathParams, pt)
	}
}

// rustPathParamParts returns "name: type" entries for each :path param.
func rustPathParamParts(allPathParams []string, pt map[string]string) []string {
	var parts []string
	for _, p := range allPathParams {
		raw := strings.TrimPrefix(p, ":")
		parts = append(parts, rustParamName(p)+": "+rustParamType(raw, pt))
	}
	return parts
}

// rustSig renders the argument list following &self, e.g. ", a: i64, b: &str".
func rustSig(parts []string) string {
	if len(parts) == 0 {
		return ""
	}
	return ", " + strings.Join(parts, ", ")
}

// rustPathExpr returns the path expression to pass to a ClientInner call.
// With no params it is a string literal; otherwise a format! borrow.
func rustPathExpr(fullPath string, allPathParams []string, pt map[string]string) string {
	if len(allPathParams) == 0 {
		return fmt.Sprintf("%q", fullPath)
	}
	tmpl := fullPath
	var args []string
	for _, p := range allPathParams {
		tmpl = strings.Replace(tmpl, p, "{}", 1)
		raw := strings.TrimPrefix(p, ":")
		name := rustParamName(p)
		if rustParamType(raw, pt) == "&str" && raw == "nodeId" {
			args = append(args, "crate::http::encode_segment("+name+")")
		} else {
			args = append(args, name)
		}
	}
	return fmt.Sprintf("&format!(%q, %s)", tmpl, strings.Join(args, ", "))
}

func rustReturnResponseType(ep Endpoint, resName string) string {
	if responseIsIDOnly(ep.Response) {
		return "IdResponse"
	}
	return customResponseTypeName(resName, ep.Action)
}

func writeRustBodyMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	reqType := requestTypeName(resName, ep.Action)
	pathExpr := rustPathExpr(fullPath, allPathParams, pt)
	verb := strings.ToLower(ep.Method)

	parts := append(rustPathParamParts(allPathParams, pt), "req: &"+reqType)
	retType := "()"
	void := true
	if ep.ResponseType != "" {
		retType = rustType(ep.ResponseType)
		void = false
	} else if len(ep.Response) > 0 {
		retType = rustReturnResponseType(ep, resName)
		void = false
	}

	fmt.Fprintf(w, "    pub async fn %s(&self%s) -> Result<%s, Error> {\n", methodName, rustSig(parts), retType)
	if void {
		// Discard the (ignored) response body via serde_json::Value, mirroring
		// the Go SDK which never decodes data for void endpoints.
		fmt.Fprintf(w, "        self.inner.%s::<serde_json::Value, _>(%s, req).await.map(|_| ())\n", verb, pathExpr)
	} else {
		fmt.Fprintf(w, "        self.inner.%s(%s, req).await\n", verb, pathExpr)
	}
	w.WriteString("    }\n")
}

func writeRustGetMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, pt map[string]string) {
	pathExpr := rustPathExpr(fullPath, allPathParams, pt)
	parts := rustPathParamParts(allPathParams, pt)
	fmt.Fprintf(w, "    pub async fn %s(&self%s) -> Result<%s, Error> {\n", methodName, rustSig(parts), rustType(ep.ResponseType))
	fmt.Fprintf(w, "        self.inner.get(%s, &[]).await\n", pathExpr)
	w.WriteString("    }\n")
}

// writeRustToggleMethod handles no-request endpoints with an inline response
// body (e.g. lock/unlock returning {id}, or a GET stats with inline fields).
func writeRustToggleMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	pathExpr := rustPathExpr(fullPath, allPathParams, pt)
	parts := rustPathParamParts(allPathParams, pt)
	fmt.Fprintf(w, "    pub async fn %s(&self%s) -> Result<%s, Error> {\n", methodName, rustSig(parts), rustReturnResponseType(ep, resName))
	switch strings.ToUpper(ep.Method) {
	case "GET":
		fmt.Fprintf(w, "        self.inner.get(%s, &[]).await\n", pathExpr)
	case "DELETE":
		fmt.Fprintf(w, "        self.inner.delete(%s).await\n", pathExpr)
	default:
		fmt.Fprintf(w, "        self.inner.post_empty(%s).await\n", pathExpr)
	}
	w.WriteString("    }\n")
}

func writeRustVoidMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, pt map[string]string) {
	pathExpr := rustPathExpr(fullPath, allPathParams, pt)
	parts := rustPathParamParts(allPathParams, pt)
	fmt.Fprintf(w, "    pub async fn %s(&self%s) -> Result<(), Error> {\n", methodName, rustSig(parts))
	// Discard the (ignored) response body via serde_json::Value, mirroring the
	// Go SDK which never decodes data for void endpoints.
	switch strings.ToUpper(ep.Method) {
	case "GET":
		fmt.Fprintf(w, "        self.inner.get::<serde_json::Value>(%s, &[]).await.map(|_| ())\n", pathExpr)
	case "DELETE":
		fmt.Fprintf(w, "        self.inner.delete::<serde_json::Value>(%s).await.map(|_| ())\n", pathExpr)
	default:
		fmt.Fprintf(w, "        self.inner.post_empty::<serde_json::Value>(%s).await.map(|_| ())\n", pathExpr)
	}
	w.WriteString("    }\n")
}

// writeRustArrayMethod handles GET endpoints returning Vec<T>, with optional
// query params exposed as method parameters.
func writeRustArrayMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, pt map[string]string) {
	pathExpr := rustPathExpr(fullPath, allPathParams, pt)
	retType := "Vec<" + rustType(ep.ResponseType) + ">"

	parts := rustPathParamParts(allPathParams, pt)
	for _, qs := range ep.Query {
		f := parseField(qs)
		parts = append(parts, rustFieldName(f.Name)+": "+rustQueryParamType(f))
	}
	fmt.Fprintf(w, "    pub async fn %s(&self%s) -> Result<%s, Error> {\n", methodName, rustSig(parts), retType)
	writeRustQueryBuild(w, ep.Query)
	fmt.Fprintf(w, "        self.inner.get(%s, &query).await\n", pathExpr)
	w.WriteString("    }\n")
}

func writeRustPageListMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	pathExpr := rustPathExpr(fullPath, allPathParams, pt)
	retType := "PaginatedResponse<" + rustType(ep.ResponseType) + ">"

	optsType := "ListOptions"
	if hasExtraQueryParam(ep.Query) {
		optsType = listOptionsTypeName(resName)
	}

	parts := append(rustPathParamParts(allPathParams, pt), "opts: Option<&"+optsType+">")
	fmt.Fprintf(w, "    pub async fn %s(&self%s) -> Result<%s, Error> {\n", methodName, rustSig(parts), retType)
	writeRustOptsQueryBuild(w, ep.Query)
	fmt.Fprintf(w, "        self.inner.get(%s, &query).await\n", pathExpr)
	w.WriteString("    }\n")
}

func writeRustCursorListMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	pathExpr := rustPathExpr(fullPath, allPathParams, pt)
	retType := "CursorPaginatedResponse<" + rustType(ep.ResponseType) + ">"

	parts := append(rustPathParamParts(allPathParams, pt), "opts: Option<&"+listOptionsTypeName(resName)+">")
	fmt.Fprintf(w, "    pub async fn %s(&self%s) -> Result<%s, Error> {\n", methodName, rustSig(parts), retType)
	writeRustOptsQueryBuild(w, ep.Query)
	fmt.Fprintf(w, "        self.inner.get(%s, &query).await\n", pathExpr)
	w.WriteString("    }\n")
}

// writeRustQueryMethod handles non-paginated GET endpoints with query params
// exposed as method parameters (e.g. Discover.meta, ClientSessions.summary).
func writeRustQueryMethod(w *strings.Builder, methodName string, ep Endpoint, fullPath string, allPathParams []string, resName string, pt map[string]string) {
	pathExpr := rustPathExpr(fullPath, allPathParams, pt)

	retType := rustType(ep.ResponseType)
	if ep.ResponseType == "" {
		retType = rustReturnResponseType(ep, resName)
	}

	parts := rustPathParamParts(allPathParams, pt)
	for _, qs := range ep.Query {
		f := parseField(qs)
		parts = append(parts, rustFieldName(f.Name)+": "+rustQueryParamType(f))
	}
	fmt.Fprintf(w, "    pub async fn %s(&self%s) -> Result<%s, Error> {\n", methodName, rustSig(parts), retType)
	writeRustQueryBuild(w, ep.Query)
	fmt.Fprintf(w, "        self.inner.get(%s, &query).await\n", pathExpr)
	w.WriteString("    }\n")
}

// rustQueryParamType is the method-parameter type for a query field passed
// directly as an argument: required values are owned/borrowed, optionals wrap.
func rustQueryParamType(f Field) string {
	base := rustType(f.Type)
	if base == "String" {
		base = "&str"
	}
	if f.Required {
		return base
	}
	return "Option<" + base + ">"
}

// writeRustQueryBuild emits the `let mut query` block for query params passed as
// method arguments (array/non-paginated query methods).
func writeRustQueryBuild(w *strings.Builder, query []string) {
	w.WriteString("        let mut query: Vec<(&str, String)> = Vec::new();\n")
	for _, qs := range query {
		f := parseField(qs)
		name := rustFieldName(f.Name)
		if f.Required {
			fmt.Fprintf(w, "        query.push((%q, %s.to_string()));\n", f.Name, name)
		} else {
			fmt.Fprintf(w, "        if let Some(v) = %s {\n", name)
			fmt.Fprintf(w, "            query.push((%q, v.to_string()));\n", f.Name)
			w.WriteString("        }\n")
		}
	}
}

// writeRustOptsQueryBuild emits the `let mut query` block sourced from an
// optional `opts` struct (page/cursor list methods).
func writeRustOptsQueryBuild(w *strings.Builder, query []string) {
	w.WriteString("        let mut query: Vec<(&str, String)> = Vec::new();\n")
	w.WriteString("        if let Some(opts) = opts {\n")
	for _, qs := range query {
		writeRustOptsPush(w, parseField(qs), "            ")
	}
	w.WriteString("        }\n")
}

// writeRustOptsPush emits a push from an `opts` struct field (list options).
func writeRustOptsPush(w *strings.Builder, f Field, indent string) {
	name := rustFieldName(f.Name)
	if f.Required {
		fmt.Fprintf(w, "%squery.push((%q, opts.%s.to_string()));\n", indent, f.Name, name)
		return
	}
	fmt.Fprintf(w, "%sif let Some(v) = &opts.%s {\n", indent, name)
	fmt.Fprintf(w, "%s    query.push((%q, v.to_string()));\n", indent, f.Name)
	fmt.Fprintf(w, "%s}\n", indent)
}
